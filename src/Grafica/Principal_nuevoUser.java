    private void jButtonEnviarAltaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEnviarAltaActionPerformed
        
        // revisamos que almenos haya un clave y user
        validacion =false;
        if(!"".equals(jTextFieldUsuario.getText()) && !"".equals(jTextFieldClave.getText()) ) validacion =true;
        
        if (validacion) {
            
            jLabelValidacion.setForeground(Color.GREEN);
            jLabelValidacion.setText("Los datos serÃ¡n enviados a la Base de datos. espere..." + validacion);
            jButtonEnviarAlta.setEnabled(false);            
            int result = persistirDatos();
            insert=(result >= 1);
            if (insert) {
                jButtonEnviarAlta.setEnabled(false);
                jButtonVolverLogin.setVisible(true);
                jButtonVolverLogin.setEnabled(true);
                jLabelInsert.setForeground(Color.GREEN);
                jLabelInsert.setText("Los Datos ya se han registrado en la Base de datos. "+result);
                
                boolean continuar = MetodosEstaticosDialogos.avisoDialogo(this, "Aceptar", "SerÃ¡ reenviado a la pantalla Login");
                this.dispose();
                
                Grafica.Frames.Dialogo_principal_login = new Principal_login();
                Grafica.Frames.Dialogo_principal_login.setVisible(true);
                               
            } else {
                jLabelInsert.setText("No se ha podido acceder a la base de Datos, intentelo en otro momento");
                jLabelInsert.setForeground(Color.RED);
            }

        } else {
            jLabelValidacion.setForeground(Color.red);
            jLabelValidacion.setText("Por favor revise bien los datos." + validacion);

        }


    }//GEN-LAST:event_jButtonEnviarAltaActionPerformed

