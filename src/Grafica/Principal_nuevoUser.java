    private int persistirDatos(){  
        int result=0;
        String user = jTextFieldUsuario.getText();
        String clave = jTextFieldClave.getText();
        usuario = new Usuario();
        usuario.setClave(clave);
        usuario.setNombre(user);
        usuario.setActivo(true);
        // si no existe el CLIENTE
        if ((MetodosWeb.MetodosWeb.login(user, clave)).getId()== 0){
        int idUsuario = MetodosWeb.MetodosWeb.guardarUsuario(usuario);
        usuario.setId(idUsuario);
        if(idUsuario>0)result++;
        direccion = new Direccion();
        direccion.setCallePiso(jTextFieldCallePiso.getText());
        direccion.setCiudad( jTextFieldCiudad.getText());
        direccion.setCodigoPostal( jTextFieldCP.getText());
        direccion.setComunidadAutonoma( jTextFieldComunidadAutonoma.getText());
        direccion.setPais( jTextFieldPais.getText());
        direccion.setActivo(true);
        int idDireccion = MetodosWeb.MetodosWeb.guardaDireccion(direccion);
        direccion.setId(idDireccion);
        if(idDireccion>0 )result++;
        cliente = new Cliente();
        cliente.setApellidos(jTextFieldApellidos.getText());
        cliente.setVendedor( jCheckBoxIsVendedor.isSelected());
        cliente.setDescripcion(jTextArea_descripcion.getText());
        cliente.setNombre(jTextFieldNombre.getText());
        cliente.setIdentificacionFiscal(jTextFieldCIF.getText());       
        cliente.setUsuario(usuario);
        cliente.setDireccion(direccion);
        cliente.setActivo(true);        
        int idCliente = MetodosWeb.MetodosWeb.guardaCliente(cliente);
        cliente.setId(idCliente);
        if(idCliente>0){
            result=idCliente;
            String mensaje="Se ha dado de alta en nuestra web... ' plantilla....'";
            new HiloEnviarMail ("rogasturias@gmail.com", "alta en APP Ca-Guela", mensaje,result).start();
            
        }
        
        }
        // si ya existe
        else {
            boolean resultado = MetodosEstaticosDialogos.avisoDialogo(this, "Re-Intentar","Salir", "El nombre de usuario y/o clave ya se han registrado+\n modifique los datos.");
            if(!resultado){
                this.dispose();
                Grafica.Frames.Dialogo_principal_login.setVisible(true);
            }
        }
        return result;
    }

