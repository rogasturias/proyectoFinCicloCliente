    private void elegirRol() {
        int resultado = -1;
        while (resultado == -1) {
            if (Datos.rolVender) {
                resultado = MetodosEstaticosDialogos.eligeCombo(this, "Aceptar", "Cancelar", Datos.OPCIONES_VENDEDOR);
                System.out.println("*********************************" + Datos.OPCIONES_VENDEDOR[resultado]);
                switch (resultado) {
                    case 0:
                        if(Grafica.Frames.Dialogo_vender_Productos == null){
                        Grafica.Frames.Dialogo_vender_Productos = new Vender_Productos();
                        }
                        Grafica.Frames.Dialogo_vender_Productos.setVisible(true);
                        this.dispose();
                        break;
                    // en este caso coinciden, pero en el futuro podria ser pantalla distinta.        
                    case 1:
                        if(Grafica.Frames.Dialogo_vender_Productos == null){
                        Grafica.Frames.Dialogo_vender_Productos = new Vender_Productos();
                        }
                        Grafica.Frames.Dialogo_vender_Productos.setVisible(true);
                        this.dispose();
                        break;

                    case 2:
                        boolean confirm = MetodosEstaticosDialogos.avisoDialogo(this, "Salir", "Cancelar", "Deseas abandonar la applicacion?");
                        if (confirm) {
                            this.dispose();
                            System.exit(0);
                            break;
                        }

                }

            } else {
                resultado = MetodosEstaticosDialogos.eligeCombo(this, "Aceptar", "Cancelar", Datos.OPCIONES_COMPRADOR);
                System.out.println("*********************************" + Datos.OPCIONES_VENDEDOR[resultado]);
                switch (resultado) {
                    case 0:
                        
                        int opcion = MetodosEstaticosDialogos.dialogoComboEligeVendedor(this);
                        System.out.println("/////////////////////////////// has optado por elegir "+opcion);
                        
                        if(opcion >= 0){
                            
                        Datos.VENDEDOR= Datos.todosVendedores.get(opcion);
                        System.out.println("ha elegido como vendedor para su pedido a "+Datos.VENDEDOR.getNombre());
                        new HiloSeleccionVendedor(Datos.VENDEDOR).start();
                        Grafica.Frames.DialogoNuevoPedido = new Nuevo_pedido();
                        Grafica.Frames.DialogoNuevoPedido.setVisible(true);
                        this.dispose();
                        }
                        break;
                        
                       
                        
                    case 1:
                        Grafica.Frames.Dialogo_revisar_Pedido_Comprador = new Revisar_Pedido_Comprador();
                        Grafica.Frames.Dialogo_revisar_Pedido_Comprador.setVisible(true);
                        this.dispose();
                        break;
                    case 2:
                        boolean result2 = MetodosEstaticosDialogos.avisoDialogo(this, "Salir", "Cancelar", "Deseas abandonar la applicacion? " + resultado);
                        if (result2) {
                            this.dispose();
                            System.exit(0);
                            break;

                        }

                }
            }
        }
    }


