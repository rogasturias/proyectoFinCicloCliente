    private void jButtonLogin1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLogin1ActionPerformed
        // consultamos con la base de datos el usuario y contraseña

        String usuario = jTextFieldUsuario.getText();
        String clave = new String(jTextFieldpswd.getPassword());
        Cliente cliente = new Cliente();
        boolean consultaBBDD = false;

        if (!"".equals(usuario) && !"".equals(clave)) {
            // nos devolvera el CLIENTE correspondiente, sino devuelve un CLIENTE nuevo con id=0
            cliente = MetodosWeb.MetodosWeb.login(usuario, clave);
            System.out.println("*********************************"+cliente.getNombre()+cliente.getId());
            consultaBBDD = cliente.getId() > 0;
        }

        // si está registrado
        if (consultaBBDD) {
            // guardamos los datos.            
            LogicaDatos.Datos.Datos.CLIENTE = cliente;          
           
            
            // siendo VENDEDOR, le preguntamos si quiere rol de VENDEDOR o de comprador
            LogicaDatos.Datos.Datos.rolVender = false;
            if (LogicaDatos.Datos.Datos.CLIENTE.isVendedor()) {
                // le preguntamos si quiere usar rol de comprador o isVendedor
                boolean rolVendedor = MetodosEstaticosDialogos.avisoDialogo(this, "Si!, Vender", "No!, Comprar", "Quieres Vender o comprar?");
                LogicaDatos.Datos.Datos.rolVender = rolVendedor;
                
            }
             // para cargar datos del CLIENTE
            new HiloSeleccionCliente(cliente).start();
            // preguntamos que quiere hacer, en funcion de si es comprador o VENDEDOR, hasta que elige algo resultado!=-1
            elegirRol();

        } // si no es correcto damos 3 oportunidades mas.
        else {
            intentos++;
            try {
                sleep(100);
            } catch (InterruptedException ex) {
                Logger.getLogger(Principal_login.class.getName()).log(Level.SEVERE, null, ex);
            }
            jLabelInfoConexion.setText(intentos + " Revise los datos. Intentelo Nuevamente");
            if (intentos == 3) {
                jLabelInfoConexion.setText(intentos + " Revise los datos. Ultimo intento");
            }
            if (intentos > 3) {
                this.dispose();
                MetodosEstaticosDialogos.avisoDialogo(this, "Aceptar", "Se va a cerrar la aplicacion");
                System.exit(0);
            }

        }
    }//GEN-LAST:event_jButtonLogin1ActionPerformed

